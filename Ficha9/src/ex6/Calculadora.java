package ex6;

public class Calculadora {
    public double adição(double n1, double n2){
        return n1 + n2;
    }
    public double multiplicação(double n1, double n2){
        return n1*n2;
    }
    public double subtração(double n1, double n2){
        return n1 - n2;
    }
    public double divisao(double n1, double n2){
        return n1/n2;
    }
}
